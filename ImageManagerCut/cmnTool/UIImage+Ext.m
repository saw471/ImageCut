+ (UIImage *)imageWithColor:(UIColor *)color
{
    CGRect rect = CGRectMake(0, 0, 1, 1);
    UIGraphicsBeginImageContext(rect.size);
    CGContextRef context = UIGraphicsGetCurrentContext();
    
    CGContextSetFillColorWithColor(context, [color CGColor]);
    CGContextFillRect(context, rect);
    
    UIImage *image = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return image;
}

+ (UIImage *)getImageFromView:(UIView *)view
{
    UIGraphicsBeginImageContext(view.bounds.size);
    [view.layer renderInContext:UIGraphicsGetCurrentContext()];
    UIImage *image = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return image;
}


/**
 *  返回一张可以随意拉伸不变形的图片
 *
 *  @param name 图片名字
 */
+ (UIImage *)resizableImage:(NSString *)name
{
    UIImage *normal = [UIImage imageNamed:name];
    CGFloat w = normal.size.width * 0.5;
    CGFloat h = normal.size.height * 0.5;
    return [normal resizableImageWithCapInsets:UIEdgeInsetsMake(h, w, h, w)];
}

-(UIImage*)scaleImg:(CGFloat)scale{
    
    CGFloat w = self.size.width * scale;
    CGFloat h = self.size.height * scale;
    return [self resizableImageWithCapInsets:UIEdgeInsetsMake(h, w, h, w)];
}


- (UIImage *)imageTintedToColor:(UIColor *)color {
    
    UIGraphicsBeginImageContext([self size]);
    CGContextRef context = UIGraphicsGetCurrentContext();
    
    // flip to UIKit coordinates
    CGContextTranslateCTM(context, 0, [self size].height);
    CGContextScaleCTM(context, 1.0, -1.0);
    
    CGContextSetBlendMode(context, kCGBlendModeColorBurn);
    CGRect rect = CGRectMake(0, 0, [self size].width, [self size].height);
    CGContextDrawImage(context, rect, [self CGImage]);
    
    [color setFill];
    CGContextClipToMask(context, rect, [self CGImage]);
    CGContextAddRect(context, rect);
    CGContextDrawPath(context,kCGPathFill);
    
    UIImage *colored = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return colored;
}


